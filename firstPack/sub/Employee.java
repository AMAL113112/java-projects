package firstPack.sub;

public class Employee {
	private int empId;
	private String name;
	
	//setter method
	public void setEmpId(int empId) {
		this.empId=empId;
	}
	public void setEmpName(String name) {
		this.name=name;
	}
	
	//Getter method
	public int getEmpId() {
		return empId;
	}
	public String getEmpName() {
		return name;
	}
	
}
